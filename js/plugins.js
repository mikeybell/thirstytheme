function stickyRelocate() {
    var windowTop = $(window).scrollTop();
    var divTop = $('#sticky-anchor').offset().top;
    if (windowTop > divTop) {
        $('nav').addClass('stick');
    } else {
        $('nav').removeClass('stick');
    }
}



